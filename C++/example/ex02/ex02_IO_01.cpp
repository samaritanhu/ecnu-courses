// 简单的输入输出 
#include<iostream>

using namespace std; 

int main()
{
    double x, y; 
    
    x = 3.14159;
    
    cout << "x=" << x << endl;
    
    y = 2.71828;
    cout << "x=" << x << " y=" << y << "\n";
    cout << "x=" << x << "\t y=" << y << "\n";
    
    cout << "The double quotation mark is \" \n";
        
    return 0; 
}
